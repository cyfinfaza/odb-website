	<h2>Our Mission</h2>
	<p>
		Our Daily Bread was established in 1983 by the nine Catholic churches of York County, in
		conjunction with the local Catholic Charities. From the very beginning it was the intention of
		the founding group to establish the program on an ecumenical basis and involve as many Jewish
		and Protestant congregations as well as the Catholic churches. Now more than 60 church and civic
		groups have been recruited to participate by supplying and serving meals. Many more churches and organizations provide financial support for Our Daily Bread.
	</p>
	<p>
		Our Daily Bread Soup Kitchen became a reality on September 13, 1983. That day, 12 guests came to the basement of Cristo Salvador Church at 235 East South Street, York, Pennsylvania.  There they enjoyed a nutritious hot meal served by Paula Moyer Koch, the manager, and volunteers from a local church.
	</p>
	<p>
		On September 12, 2021, the 38th anniversary of Our Daily Bread, it is important to note that
		over 2.5 million meals have been served, or a daily average of 286.
	</p>
	<p>As of September 12, 2022, the 39th anniversary of Our Daily Bread, we have served over 2.5 million meals.</p>

